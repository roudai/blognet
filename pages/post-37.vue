    </p>
    <p>
      このOLL、PLLをすべて覚え、瞬時に回せるようになって、やっとルービックキューブ上級者の「入り口」に立つことができます。
    </p>

    <h2>世界記録のそのとき、何が起きたのか。</h2>
    <p>
      ここで、世界記録の解法を見てみましょう。ルービックキューブの解法は、このように記号で一意に表すことができます。
    </p>
    <div class="box">
      スクランブル<br />
      R2 B D2 F2 U2 R D2 L' B L' B D R' F' U B2 F L<br />

      解法<br />
      z2 // インスペクション<br />
      D' L' R' F R D2' // クロス<br />
      U2 L' U' L // 1stペア<br />
      U' U' U' F' U' F U R U' R' // 2ndペア<br />
      y R U R' // 3rdペア<br />
      U' y U' U' R U R' // 4thペア<br />
      U r U r' R U R' U' r U' r' // OLL
    </div>
    <p>
