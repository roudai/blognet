    <h2>ルービックキューブの解法 -概要-</h2>
    <h3>CFOP法</h3>
    <p>
      まずは、ルービックキューブの大会で、10秒前後で揃えるような人達が、どのような方法でルービックキューブを揃えているのかを説明します。
