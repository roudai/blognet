    <h3>インスペクション</h3>
    <p>
      解法の最初の1行、インスペクションにある"z2"はキューブの持ち替え記号で、上下逆に持つことを表します。
      ルービックキューブの公式ルールでは白色の面が上の状態を基準とするため、白面を下にして開始したことを表します。
      これは最初にキューブを見るとき（インスペクション時）に決めるため、タイムには影響しません。
    </p>

    <h3>Cross</h3>
    <p>クロスは、6手で終わっています。特別なことはしておらず、ごく普通の手法です。</p>

    <h3>F2L</h3>
    <p>
      1stペア～4thペアをしているのが分かると思います。各ペア手順の最初のほうにあるUやyの記号は上面の位置合わせや持ち替えを表しているので、
      実際にF2Lをやっているのはそれ以降になります。つまりF2L手順としては、
    </p>
    <div class="box">
      L' U' L // 1stペア<br />
      F' U' F U R U' R' // 2ndペア<br />
      R U R' // 3rdペア<br />
      R U R' // 4thペア<br />
    </div>
    <p>となります。</p>
    <p>
      この中で2ndペアでは少しテクニカルな方法を用いていますが、1st、3rd、4thのペアはすべて3手で終わっており、これはF2Lの中で最も短い手数です。
      3rd、4thまで読んで2ndペアでテクニカルなことをしたのかは不明ですが、この時点でかなりの幸運であったことは間違いありません。
    </p>

    <h3>OLL</h3>
    <p>
      次に、OLLです。そして、解法は何故かOLLで終わっており、PLLが書かれていません。これは、OLLが終わった時点でPLLがすでにない状態になり、
      完成してしまったことを表します（これをPLLスキップと呼びます）。PLLをするだけでもどれだけ早い人でも1秒前後かかるため、このPLLスキップが起きたために好タイムがでました。
    </p>
    <p>
      ルービックキューブの、特に単発での記録ではやはりこういった「運」の要素が非常に大きく、単発の世界記録、日本記録等の多くの記録は、
      このように途中や最後のステップがなかったことによるラッキーで更新されることがほとんどです。
    </p>

