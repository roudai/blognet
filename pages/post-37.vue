      </div>
    </p>
    <p>
      これをすべて覚えて、瞬時にどのパターンか判断し、手順を回すというのは、なかなか大変です。しかし、ルービックキューブを競技として本気でやるのであれば、いずれ通らなければならない道です。
    </p>

    <h3>PLL</h3>
    <p>最後の層の場所を揃える「PLL」では、そのパターン数は<b>21</b>あります。</p>
