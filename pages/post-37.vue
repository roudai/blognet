    <h2>ラッキーでは済まされない、超高等テクニック</h2>
    <p>
      ここまで読むと、「なんだ、ただラッキーなだけで世界記録更新したんじゃん」と思うことでしょう。しかしその裏には、想像を超える超高等テクニックが使われています。
    </p>

    <h3>超高等テクニック : OLLCP</h3>
    <p>
      OLLとは、「最後の層の向きを揃える」というステップでした。しかし近年では、このOLLをさらに発展させた、「<b>OLLCP</b>」というものが開発されています。
    </p>
    <p>
      OLLCPとは、<b>O</b>rientation of <b>L</b>ast <b>L</b>ayer and <b>C</b>orner <b>P</b>ermutation
      のことで、その字面通り、「最後の層の向きを合わせかつ、コーナーの場所を揃える(Corner Permutation)」ことをいいます。
    </p>
    <p>
      OLLは全部で57パターンあると紹介しましたが、OLLCPはこのOLLをさらに細分化して、全部で<b>331</b>パターンあります。
    </p>
    <p>例えば、</p>
    <div class="columns ml-0 mr-0">
      <img src="@/assets/37/OLL16.webp" width="80px" />
    </div>
    <p>このようなパターンのOLLがあるのですが、これがOLLCPになると</p>
    <div class="columns ml-0 mr-0">
      <img src="@/assets/37/ollcp.webp" width="240px" />
    </div>
    <p>さらに6つに細分化されます。向きだけではなく、コーナーの色まで見て判断する必要があります。</p>
    <p>
      見ての通り、OLLCPの判断はとても難しく、また300以上というパターン数は、想像を絶する数です。
      OLLCPという考え自体が提案されたのが2011年頃とかなり新しい手法なこともあり、このパターンをすべて覚え、実践している人が世界にいるのかは不明です。
      しかし、トップレベルの選手達は、少しずつではありながら、着実にこれを使いこなしつつあります。
    </p>
    <p>そんな膨大なパターン数と、激ムズな判断をしてまで、OLLCPを使おうとするのはなぜでしょうか？</p>
    <p>
      その一番の理由は、<br />
      <b>PLLスキップの確率が格段に上がる </b><br />
      ことです。
    </p>
    <p>
      タイムがどんどん縮まっている現在、ただ運任せにラッキーなスキップを待つよりも、可能な限り<b>意図的にラッキーを作り出そう</b>としているのです。
    </p>
    <p>
      普通にOLLをした場合、PLLスキップする確率は<b>1/72</b>、<b>約1.4%</b>です（PLLは21パターンですが、対称性などを考慮するとこの確率になります）。
    </p>
    <p>
      それが、OLLCPを使うことで、PLLスキップの確率は<b>1/12</b>、<b>約8.3%</b>と、<b>6倍</b>にも跳ね上がります。
      トップレベルの選手たちは、こうやって少しでもラッキーなケースを引き当てようとしています。
    </p>

