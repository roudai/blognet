    <h3>Last Layerの最終形態 : 1LLL</h3>
    <p>
      それは、LucasがOLLCPをしながらも、さらにPLLスキップが起こることまで読み切っていたからとしか考えられません。
      最後の層を一気に揃える、つまりOLLとPLLを同時にすることを、<b>1LLL(1 Look Last Layer)</b>と呼びます。
    </p>
    <p>
      もちろんそのパターン数はOLLCPと比べ物にならないほど多く、対称なものや逆回しのものを同一とみた場合でも<b>1211</b>パターン、
      それらを別のもとした場合<b>3915</b>パターンにもなります。もちろん、これをすべて使いこなす人はまだ誰もいません
      （コンピュータを使って手順の解析、洗い出しはされています）。
    </p>
    <p>
      Lucasは、OLLCPをしながらも、その形のその手順が1LLLであることを（偶然）知っていた、と思われます。
      もちろんすべてのパターンを知っているわけではありませんが、何千回何万回と練習をしている中で、それが分かったのでしょう。
    </p>
    <p>
      5.25秒と4.90秒の差はわずか0.35秒ですが、その0.35秒を縮めるためにはこれほどまでの高等な技術が要求されるのです。
    </p>

