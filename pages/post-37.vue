    <h3>Cross</h3>
    <p>
      最初のクロスですが、これはパターンを覚えるというよりは、経験数・練習数がものをいいます。うまくやれば、だいたい<b>5～8手</b>くらいで揃えることができます。
    </p>
    <p>
      ルービックキューブの公式競技では、タイムを計り始める前に最大15秒間のキューブを見て考える時間（インスペクションタイム）があるのですが、上級者はその時間で、このクロスを読み切ります。
    </p>
    <p>逆に言えば、それ以上は15秒では読めません。超上級者で次のF2Lのひとつ目まで読めればかなりすごいです。</p>

    <h3>F2L</h3>
    <p>
      次の「F2L」では、下の2段を揃えます。クロスを揃えた時点でそのクロスの周りに4つの「スロット」があるので、そこにひとつひとつ入れていきます。
      合計で4つのスロットがあり、ペアのパーツを入れていくので、それぞれを<b>1stペア</b>、<b>2ndペア</b>、<b>3rdペア</b>、<b>4thペア</b>と呼びます。
      1つのスロットを入れるので最短で<b>3手</b>、長いと<b>10手前後</b>かかります。
    </p>

    <h3>OLL</h3>
    <p>
      次は最後の層の向きを揃える「OLL」です。ルービックキューブがある程度早い人達（だいたい、20秒を切るあたり）は、
      最後の層の向きがバラバラの状態から、一気に向きを揃える（上面の色を揃える）パターンと、その手順を、すべて覚えています。そのパターンの数は、<b>57</b>です。
    </p>
    <p>
      参考 : Cube Voyage
