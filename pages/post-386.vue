    <p>VLSの元となったのは、2005年にLucas Winterが提案した<b>Winter Variation(WV)</b>というテクニックでした。</p>
    <p>
      Winter Variationの手順については、私が管理しているルービックキューブ情報サイト、roudai.netでも記載しています。
    </p>
    <p><a href="https://roudai.net/f2l/winter-variation/" target="_blank">roudai.net - Winter Variation</a></p>
    <p>
      Winter Variationは、<br />
      「最後のF2Lを入れるとき、<b>かつ上面のエッジ向きがすべて正しいときに</b>、OLLをスキップさせる手順」<br />
      のことをいいます。
    </p>
    <p>
      VLSは、このWinter
      Variationから「上面のエッジ向きがすべて正しいとき」という条件を除いて、「上面のエッジ向きにかかわらず」OLLをスキップさせる手順です。
    </p>
    <p>
      Winter Variationは全部で<b>27パターン</b>ですが、Valk Last SlotではWinter
      Variationを含む全<b>216</b>までパターン数が増えます。
    </p>
    <p>
