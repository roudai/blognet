      スクランブルアルゴリズムに関係なく、WCA
      代理人は競技者に対し解法中の各手順の意図の説明を求めることができる。」というレギュレーションもあるため、この記録は大会のWCA
      代理人によって一度は認められました。
    </p>
    <p>
      しかしWCAは、このレギュレーションに不備があったことを認めた上で、Linusのこの記録を無効とすることを決めました。
    </p>
